clear all; close all; clc; 
%% Master Dataset Generator 
num_simulations = 2; 
model_name = 'Nalanchira_RMU_';
load_system(model_name);
set_param(model_name, 'Dirty', 'off');

All_Results = cell(num_simulations, 1); 
h = waitbar(0, 'Initializing Simulations...');
temp_map = [101, 101, 102, 102, 103, 104, 104, 105, 106, 107, 108, 109, 110, 111, 112];
ds = 20;

for i = 1:num_simulations
    % 1. INITIALIZE the simIn object FIRST
    simIn = Simulink.SimulationInput(model_name);
    
    % 2. Generate random parameters
    current_fault_id = randi([1, 15]);
    current_start    = 0.3 + (0.4 * rand); 
    current_duration = 0.3 + (0.1 * rand); 
    random_phase     = rand * 360; % Point-on-wave variation
    
    % 3. Set variables in the simulation input
    simIn = simIn.setModelParameter('StopTime', '1');
    simIn = simIn.setVariable('cfg_fault_id', current_fault_id);
    simIn = simIn.setVariable('cfg_start', current_start);
    simIn = simIn.setVariable('cfg_duration', current_duration);
    simIn = simIn.setVariable('cfg_source_phase', random_phase); 
    
    try
        simOut = sim(simIn); 
        
        % --- ROBUST DATA EXTRACTION ---
        extract = @(x) x(1:ds:end);
        
        % MUST MATCH SIMULINK "TO WORKSPACE" VARIABLE NAMES EXACTLY
        sigs = { ...
            simOut.Va_grid1, simOut.Vb_grid1, simOut.Vc_grid1, simOut.Ia_grid1, simOut.Ib_grid1, simOut.Ic_grid1, ...
            simOut.VMag_grid1_pos, simOut.VMag_grid1_neg, simOut.VMag_grid1_zero, ...
            simOut.VPh_grid1_pos, simOut.VPh_grid1_neg, simOut.VPh_grid1_zero, ...
            simOut.IMag_grid1_pos, simOut.IMag_grid1_neg, simOut.IMag_grid1_zero, ...
            simOut.IPh_grid1_pos, simOut.IPh_grid1_neg, simOut.IPh_grid1_zero, ...
            ...
            simOut.Va_grid2, simOut.Vb_grid2, simOut.Vc_grid2, simOut.Ia_grid2, simOut.Ib_grid2, simOut.Ic_grid2, ...
            simOut.VMag_grid2_pos, simOut.VMag_grid2_neg, simOut.VMag_grid2_zero, ...
            simOut.VPh_grid2_pos, simOut.VPh_grid2_neg, simOut.VPh_grid2_zero, ...
            simOut.IMag_grid2_pos, simOut.IMag_grid2_neg, simOut.IMag_grid2_zero, ...
            simOut.IPh_grid2_pos, simOut.IPh_grid2_neg, simOut.IPh_grid2_zero, ...
            ...
            simOut.Va_hos, simOut.Vb_hos, simOut.Vc_hos, simOut.Ia_hos, simOut.Ib_hos, simOut.Ic_hos, ...
            simOut.VMag_hos_pos, simOut.VMag_hos_neg, simOut.VMag_hos_zero, ...
            simOut.VPh_hos_pos, simOut.VPh_hos_neg, simOut.VPh_hos_zero, ...
            simOut.IMag_hos_pos, simOut.IMag_hos_neg, simOut.IMag_hos_zero, ...
            simOut.IPh_hos_pos, simOut.IPh_hos_neg, simOut.IPh_hos_zero, ...
            ...
            simOut.Va_res, simOut.Vb_res, simOut.Vc_res, simOut.Ia_res, simOut.Ib_res, simOut.Ic_res, ...
            simOut.VMag_res_pos, simOut.VMag_res_neg, simOut.VMag_res_zero, ...
            simOut.VPh_res_pos, simOut.VPh_res_neg, simOut.VPh_res_zero, ...
            simOut.IMag_res_pos, simOut.IMag_res_neg, simOut.IMag_res_zero, ...
            simOut.IPh_res_pos, simOut.IPh_res_neg, simOut.IPh_res_zero, ...
            simOut.Fault_Present, ... 
            simOut.Fault_Type, ...
            simOut.Fault_Location...
        };

        sigs_ds = cellfun(extract, sigs, 'UniformOutput', false);
        min_len = min(cellfun(@length, sigs_ds));
        num_signals = numel(sigs_ds); 
        
        final_matrix = zeros(min_len, num_signals); 
        for j = 1:num_signals
            temp_sig = sigs_ds{j};
            final_matrix(:, j) = temp_sig(1:min_len);
        end
        
        % Correct identification of fault type and location
        %current_location = temp_map(current_fault_id);
        
        % Create columns for Fault_Type and Location
       % f_type_col = ones(min_len, 1) * current_fault_id;
       % f_loc_col  = ones(min_len, 1) * current_location;
        
        %All_Results{i} = single([final_matrix, f_type_col, f_loc_col]);
        run_id_col = ones(min_len, 1, 'single') * i;
        All_Results{i} = single([run_id_col, final_matrix]);
        All_Results{i} = single(final_matrix);
        
    catch ME
        fprintf('Sim %d failed: %s\n', i, ME.message);
    end
    waitbar(i/num_simulations, h, sprintf('Simulating %d of %d...', i, num_simulations));
end
close(h);

%% Export
valid_results = ~cellfun(@isempty, All_Results);
if any(valid_results)
    All_Data = cell2mat(All_Results(valid_results));
    
    % Headers must match the sigs order exactly
    Column_Names = { ...
        'Run_ID', ...
        'Va_G1','Vb_G1','Vc_G1','Ia_G1','Ib_G1','Ic_G1', ...
        'VM_G1P','VM_G1N','VM_G1Z','VP_G1P','VP_G1N','VP_G1Z', ...
        'IM_G1P','IM_G1N','IM_G1Z','IP_G1P','IP_G1N','IP_G1Z', ...
        'Va_G2','Vb_G2','Vc_G2','Ia_G2','Ib_G2','Ic_G2', ...
        'VM_G2P','VM_G2N','VM_G2Z','VP_G2P','VP_G2N','VP_G2Z', ...
        'IM_G2P','IM_G2N','IM_G2Z','IP_G2P','IP_G2N','IP_G2Z', ...
        'Va_Hos','Vb_Hos','Vc_Hos','Ia_Hos','Ib_Hos','Ic_Hos', ...
        'VM_HosP','VM_HosN','VM_HosZ','VP_HosP','VP_HosN','VP_HosZ', ...
        'IM_HosP','IM_HosN','IM_HosZ','IP_HosP','IP_HosN','IP_HosZ', ...
        'Va_Res','Vb_Res','Vc_Res','Ia_Res','Ib_Res','Ic_Res', ...
        'VM_ResP','VM_ResN','VM_ResZ','VP_ResP','VP_ResN','VP_ResZ', ...
        'IM_ResP','IM_ResN','IM_ResZ','IP_ResP','IP_ResN','IP_ResZ', ...
        'Fault_Active','Fault_Type','Location'};
    
    ANN_Table = array2table(All_Data, 'VariableNames', Column_Names);
    writetable(ANN_Table, 'Nalanchira_Master_Dataset.csv');
    save('Nalanchira_Master_Dataset.mat', 'ANN_Table', '-v7.3');
    disp('Success! Dataset generated with Point-on-Wave variations.');
else
    disp('Extraction failed. Check the Command Window for specific Simulink errors.');
end